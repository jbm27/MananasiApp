export function normalizeEmployeeName(name) {
  return String(name ?? '')
    .normalize('NFKD')
    .replace(/[\u0300-\u036f]/g, '')
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, ' ')
    .trim()
    .replace(/\s+/g, ' ')
}

export function buildEmployeeIdMap(employees) {
  const map = new Map()
  for (const employee of employees) {
    const normalized = normalizeEmployeeName(employee.name)
    const nextId = WORK_NUMBER_BY_NORMALIZED_NAME[normalized]
    if (!nextId) {
      continue
    }
    if (employee.id !== nextId) {
      map.set(String(employee.id), nextId)
    }
  }
  return map
}
